# بيانات شحنة البلح السيوي
اسم_المنتج = "بلح سيوي فاخر"
الكمية_بالطن = 25
سعر_البيع_بالدولار = 1200
سعر_الدولار = 50

# التكاليف (بالدولار)
سعر_الشراء_من_المزرعة = 700
تكلفة_الشحن = 2500
تغليف_ونثريات = 1200

# الحسابات
اجمالي_المبيعات = الكمية_بالطن * سعر_البيع_بالدولار
اجمالي_التكاليف = (الكمية_بالطن * سعر_الشراء_من_المزرعة) + تكلفة_الشحن + تغليف_ونثريات
صافي_الربح = اجمالي_المبيعات - اجمالي_التكاليف

# عرض النتيجة
print("--- تقرير شحنة الفيل الأزرق ---")
print("المنتج: " + اسم_المنتج)
print("إجمالي المبيعات: $" + str(اجمالي_المبيعات))
print("صافي الربح المتوقع: $" + str(صافي_الربح))

if صافي_الربح > 5000:
    print("✅ النتيجة: صفقة ممتازة ! توكلنا على الله.")
else:
    print("⚠️ النتيجة: المكسب قليل، نحتاج مراجعة الأسعار.")
